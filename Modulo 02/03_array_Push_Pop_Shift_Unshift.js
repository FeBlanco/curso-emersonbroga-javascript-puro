/*Aprofundando no JavaScript
Array push, pop, shift, unshift*/

const data = [1, 2, 3];
data.push(4) // adiciona no final do array

data.pop() // remove o ultimo valor da fila do array

data.shift() // remove o valor do começo  do array

data.unshift(0) // adiciona no começo do array

console.log(data);
